    <h3>What it is</h3>
    <ul>
      <li><strong>Zones</strong> control parts of a sign separately or together.</li>
      <li><strong>Scenes</strong> save favourite looks.</li>
      <li><strong>Timeline</strong> sets calm mornings, clean daytime, warm evenings.</li>
      <li><strong>Sync</strong> keeps effects lined up across runs.</li>
      <li><strong>Reliable</strong> means the last scene resumes after power returns.</li>
      <li><strong>Web app</strong> means you open control in a browser.</li>
    </ul>
    <p>LED Node is a compact controller that sits near your power supply and LED drivers.</p>
    <p>Plug in the LED runs, connect to Wi-Fi, and manage every zone from a clear browser page.</p>
  </article>
  <article class="card">
    <h3>What it can do</h3>
